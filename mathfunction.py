from math import *

# square root
num1 = int(input("Please Enter the Number"))
print("Square root of ", num1, "is", sqrt(num1))
num2 = sqrt(num1)

print("Floor value of", num2, "is", floor(num2))
print("Ceiling value of", num2, "is", ceil(num2))

# square
print("Square of ", num1, "is", pow(num1, 2))

# cube
print("Cube of ", num1, "is", pow(num1, 3))

# pi
print("Value of Pie is", pi)

# e
print("Value of exponent is", e)
print("Floor value of exponent", e, "is", floor(e))
print("Ceiling value of exponent", e, "is", ceil(e))
